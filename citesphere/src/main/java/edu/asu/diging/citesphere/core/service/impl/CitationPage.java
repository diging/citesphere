package edu.asu.diging.citesphere.core.service.impl;

public class CitationPage {
    
    private String index;
    private String next;
    private String nextIndex;
    private String nextPage;
    private String prevIndex;
    private String prev;
    private String prevPage;
    private String sortBy;
    private String collectionId;
    private String zoteroGroupId;
    public String getIndex() {
        return index;
    }
    public void setIndex(String index) {
        this.index = index;
    }
    public String getNext() {
        return next;
    }
    public void setNext(String next) {
        this.next = next;
    }
    public String getNextIndex() {
        return nextIndex;
    }
    public void setNextIndex(String nextIndex) {
        this.nextIndex = nextIndex;
    }
    public String getNextPage() {
        return nextPage;
    }
    public void setNextPage(String nextPage) {
        this.nextPage = nextPage;
    }
    public String getPrevIndex() {
        return prevIndex;
    }
    public void setPrevIndex(String prevIndex) {
        this.prevIndex = prevIndex;
    }
    public String getPrev() {
        return prev;
    }
    public void setPrev(String prev) {
        this.prev = prev;
    }
    public String getPrevPage() {
        return prevPage;
    }
    public void setPrevPage(String prevPage) {
        this.prevPage = prevPage;
    }
    public String getSortBy() {
        return sortBy;
    }
    public void setSortBy(String sortBy) {
        this.sortBy = sortBy;
    }
    public String getCollectionId() {
        return collectionId;
    }
    public void setCollectionId(String collectionId) {
        this.collectionId = collectionId;
    }
    public String getZoteroGroupId() {
        return zoteroGroupId;
    }
    public void setZoteroGroupId(String zoteroGroupId) {
        this.zoteroGroupId = zoteroGroupId;
    }
    
}
